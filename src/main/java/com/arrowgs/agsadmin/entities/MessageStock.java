package com.arrowgs.agsadmin.entities;

public class MessageStock {

	private Integer	id;
	private Integer	idSkuProduct;
	private Integer idUser;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdSkuProduct() {
		return idSkuProduct;
	}
	public void setIdSkuProduct(Integer idSkuProduct) {
		this.idSkuProduct = idSkuProduct;
	}
	public Integer getIdUser() {
		return idUser;
	}
	public void setIdUser(Integer idUser) {
		this.idUser = idUser;
	}

}
